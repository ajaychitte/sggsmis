        <p>&nbsp&nbsp&nbsp The aim of this project is to Design a Management Information System (MIS) for SAE Clubs to enable the team members and faculty to store their data and manage the information regarding club members, inventory and finance so, it will be easy for them to access the required piece of information or document related to any part.</p>
        <p>This Management Information System is part of our Final Year B. Tech. Project. It is the solution for the students and staff of the college to manage the information of Society of Automotive Engineers (SAE) Clubs.</p>
        <p>The software has mainly four modules: <ul><li><b>Student Module:</b> This module manage club members information and past team records and enable user to add and modify the data.</li><li><b>Inventory Procurement Module:</b> This module provides fuctionality for managing procurement process for creating and maganing submission notes, inquires, quotation, purchase order and invoices. (ongoing)</li><li><b>Finance Module: </b>This module will deal with team expenses and bills. (Ongoing)</li><li><b>Event Module: </b> This module is to manage team's event schedules. (Ongoing)</li></ul> </p>
